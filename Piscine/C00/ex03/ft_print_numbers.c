#include <unistd.h>

void	ft_print_numbers(void);

void	ft_print_numbers(void)
{
	int		n;
	char	sn;

	n = 0;
	while (n <= 9)
	{
		sn = n + '0';
		write(1, &sn, 1);
		n++;
	}
}
