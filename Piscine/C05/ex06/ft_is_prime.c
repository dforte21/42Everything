int	ft_is_prime(int nb);

int	ft_is_prime(int nb)
{
	int	i;
	int	flag;

	i = nb;
	flag = 0;
	if (nb < 2)
		return (0);
	while (i > 0)
	{
		if ((nb % i) == 0)
			flag++;
		i--;
	}
	if (flag == 2)
		return (1);
	return (0);
}
