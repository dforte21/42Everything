#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	argc = 0;
	while (argv[0][i] != 0)
		i++;
	while (j < i)
	{
		write(1, &argv[0][j], 1);
		j++;
	}
	write(1, "\n", 1);
}
