int	ft_any(char **tab, int (*f)(char*));

int	ft_any(char **tab, int (*f)(char*))
{
	int	i;
	int	a;

	i = 0;
	while (tab[i])
	{
		a = f(tab[i]);
		if (a != 0)
			return (1);
		i++;
	}
	return (0);
}
