#include "functions.h"

int	check(char *str)
{
	int	i;

	i = ft_strlen(str);
	if (i != 31)
		return (0);
	i = 0;
	while (str[i] != 0)
	{
		if ((i % 2) == 0)
		{
			if (str[i] < 49 || str[i] > 52)
				return (0);
		}
		else
		{
			if (str[i] != 32)
				return (0);
		}
		i++;
	}
	return (1);
}
